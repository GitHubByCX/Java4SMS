package com.j4.bean;

public class Conduct {
    private Integer id;

    private Integer userId;

    private Integer caoXing;

    private Integer chiDao;

    private Integer kuangKe;

    private Integer qingJia;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getUserId() {
        return userId;
    }

    public void setUserId(Integer userId) {
        this.userId = userId;
    }

    public Integer getCaoXing() {
        return caoXing;
    }

    public void setCaoXing(Integer caoXing) {
        this.caoXing = caoXing;
    }

    public Integer getChiDao() {
        return chiDao;
    }

    public void setChiDao(Integer chiDao) {
        this.chiDao = chiDao;
    }

    public Integer getKuangKe() {
        return kuangKe;
    }

    public void setKuangKe(Integer kuangKe) {
        this.kuangKe = kuangKe;
    }

    public Integer getQingJia() {
        return qingJia;
    }

    public void setQingJia(Integer qingJia) {
        this.qingJia = qingJia;
    }
}